package com.hgl.hglrpc.bootstrap;

import com.hgl.hglrpc.RpcApplication;

/**
 * 消费者的启动流程（ConsumerBootstrap）
 *
 * <p>想象你是一个准备出门采购的消费者——你不会直接冲进商场，
 * 而是先做点准备工作：带好钱包、查好购物清单、确认一下导航地址。</p>
 *
 * <p>ConsumerBootstrap 就是消费者的"出门准备清单"。
 * 在消费者开始使用 RPC 框架远程调用服务之前，它负责把基础设施搭好：</p>
 *
 * <pre>
 *   消费者的启动之旅：
 *
 *   ┌─────────────────────────────────────────────────┐
 *   │              ConsumerBootstrap.init()           │
 *   └──────────────────────┬──────────────────────────┘
 *                          │
 *                          v
 *   ┌─────────────────────────────────────────────────┐
 *   │           RpcApplication.init()                 │
 *   │                                                 │
 *   │   1. 读取 RPC 配置（从配置文件、环境变量等）           │
 *   │   2. 初始化注册中心连接（准备查地址簿的能力）           │
 *   │                                                 │
 *   └──────────────────────┬──────────────────────────┘
 *                          │
 *                          v
 *   ┌─────────────────────────────────────────────────┐
 *   │           准备就绪！可以开始调用远程服务了            │
 *   │                                                 │
 *   │   ServiceProxyFactory.getProxy(XxxService.class)│
 *   │   xxxService.someMethod(args)                   │
 *   └─────────────────────────────────────────────────┘
 * </pre>
 *
 * <p>为什么消费者只需要初始化这么少的东西？</p>
 * <p>因为消费者不需要启动服务器、不需要注册服务到注册中心。
 * 它的角色是"调用方"，只需要知道去哪里找服务、怎么调用就够了。
 * 真正的网络通信、服务发现等逻辑，都封装在 ServiceProxy 中，
 * 在第一次发起调用时才会真正触发。</p>
 *
 * @author HGL
 * @since 2025/9/5 15:57
 */
public class ConsumerBootstrap {

    /**
     * 初始化消费者——把"钱包和清单"准备好。
     *
     * <p>这个方法通常在应用启动时调用一次，且只调用一次。
     * 它会触发 RPC 框架的全局初始化：</p>
     * <ul>
     *   <li>加载 RPC 配置（协议类型、注册中心地址、负载均衡策略等）</li>
     *   <li>初始化注册中心客户端连接</li>
     *   <li>其他一次性准备工作</li>
     * </ul>
     *
     * <p>初始化完成后，消费者就可以通过 ServiceProxyFactory
     * 创建代理对象来远程调用服务了。</p>
     */
    public static void init() {
        // RPC 框架全局初始化——读配置、连注册中心、做好一切准备
        // 就像出门前检查钱包、钥匙、手机是否都带齐了
        RpcApplication.init();
    }
}
